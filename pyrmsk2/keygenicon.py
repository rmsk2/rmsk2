def get_icon_names():
    return list(xpm_dict.keys())

## \brief This method allows to retrieve the XPM data for the desired icon.
#
#  \param [identifier] Is a string or None. Holds the symbolic name of the icon to retrieve. If None
#         is specified as the value for identifier then the XPN data for the default icon is returned.
#
#  \returns A list of strings. Contains the image data in XPM format where each line of the XPM data
#           is represented by a separate list element.
#
def get_xpm_data(identifier = None):
    data = []
    
    if identifier == None:
        identifier = DEFAULT_ICON
    
    keygen_icon_data = xpm_dict[identifier] 
    
    data_raw = binascii.a2b_base64(keygen_icon_data).decode().split('\n')    
    
    for line in data_raw:
        if line != '':
            # Get rid of the "...." that enclose the raw XPM data
            # Also do away with the commas that end all lines but the last one
            if line[-1] == ',':
                data.append(line[1:len(line) - 2])
            else:
                data.append(line[1:len(line) - 1])
    
    return data
    

