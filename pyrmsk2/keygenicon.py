## \brief Name of the default icon.
DEFAULT_ICON = 'keygenicon'

## \brief Dictionary that maps a symbolic name to base64 enoded XPM data
xpm_dict = {DEFAULT_ICON:keygen_icon_data}

## \brief This function can be used to retrieve the list of the symbolic icon names known to this repository.
#
#  \returns A list of strings.
